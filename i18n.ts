type Locale = keyof typeof translations;
type TranslationKey = keyof (typeof translations)['pl'];

type LanguageContextType = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey | string) => string;
};

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [locale, setLocaleState] = useState<Locale>(() => {
    if (typeof window !== 'undefined') {
      const savedLocale = localStorage.getItem('locale');
      return (savedLocale === 'pl' || savedLocale === 'nl' || savedLocale === 'tr' || savedLocale === 'bg') ? savedLocale as Locale : 'pl';
    }
    return 'pl';
  });

  useEffect(() => {
    if (typeof window !== 'undefined') {
      localStorage.setItem('locale', locale);
      document.documentElement.lang = locale;
    }
  }, [locale]);

  const setLocale = (newLocale: Locale) => {
    setLocaleState(newLocale);
  };

  const t = (key: TranslationKey | string): string => {
    return translations[locale][key as TranslationKey] || key;
  };

  // FIX: Using React.createElement instead of JSX to avoid parsing issues in a .ts file.
  return React.createElement(
    LanguageContext.Provider,
    { value: { locale, setLocale, t } },
    children
  );
};

export const useTranslation = (): LanguageContextType => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useTranslation must be used within a LanguageProvider');
  }
  return context;
};
